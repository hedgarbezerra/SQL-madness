 CREATE TABLE CURSO (
SG_CURSO VARCHAR(5) PRIMARY KEY,
NM_CURSO VARCHAR(50) NOT NULL);

CREATE TABLE ALUNO(
CD_MATRICULA INTEGER,
NM_ALUNO VARCHAR(50),
IC_SEXO CHAR(1),
DT_NASCIMENTO DATE DEFAULT SYSDATE,
SG_CURSO VARCHAR(5),
CONSTRAINT PK_ALUNO PRIMARY KEY (CD_MATRICULA),
CONSTRAINT FK_CURSO FOREIGN KEY (SG_CURSO) REFERENCES CURSO
);


CREATE TABLE MENSALIDADE(
DT_EMISSAO DATE,
DT_VENCIMENTO DATE,
DT_PAGAMENTO DATE,
VL_EMITIDO NUMBER(8,2),
VL_PAGO NUMBER(8,2),
CD_MATRICULA INTEGER,
CONSTRAINT FK_MENSALIDADE_ALUNO FOREIGN KEY (CD_MATRICULA) REFERENCES ALUNO(CD_MATRICULA) ,
CONSTRAINT PK_MENSALIDADE PRIMARY KEY (DT_EMISSAO,CD_MATRICULA)
);

